public class Test10 {
    public static void main(String[] args) {
        // for关键字后的小括号中由两个分号作为核心构成三个表达式
        // 表达式1是定义循环变量，可以定义多个循环变量，用逗号分割
        // 表达式2是循环条件
        // 表达式3是循环变量的改变
        // for后面的小括号中两个分号是不能删除的，但是三个表达式都是可以省略的
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

    }
}
